# Object-oriented programming (OOP) is a programming paradigm that uses objects and their interactions to design applications and computer programs.
#
# There are some basic programming concepts in OOP:
#
# Abstraction
# Polymorphism
# Encapsulation
# Inheritance
# The abstraction is simplifying complex reality by modeling classes appropriate to the problem. The polymorphism is the process of using an operator or function in different
# ways for different data input. The encapsulation hides the implementation details of a class from other objects. The inheritance is a way to form new classes using classes that
# have already been defined.

